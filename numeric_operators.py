""" Numerical operators """

print(5 + 10)  # add

print(5 - 10)  # subtract

print(5 * 10)  # multiply

print(5 / 10)  # Divide

print(5 // 10)  # Divide and return whole number

print(5 % 10)  # Modulo - Gives remainder (difference between the two but always positive)

print(5 ** 10)  # Exponent or to the power of
